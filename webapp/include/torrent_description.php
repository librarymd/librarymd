function get_mandatory_fields($cat_id) {
	global $SETTINGS_PATH;
	$cat_id = (int)$cat_id;
	$bank = unserialize(file_get_contents($SETTINGS_PATH . '/fields_mandatory.txt'));
	if(!isset($bank[$cat_id])) return;
	return $bank[$cat_id];
}

function get_all_fields($cat_id) {
